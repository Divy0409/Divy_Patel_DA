
"""
String Slicing
"""
#    01234567890123456     +
s = "Tops Technologies" 
#    76543210987654321     -


print(s)

#Positive Value
print(s[1:10])
print(s[:14])
print(s[6:])
print(s[1:13:2])
print(s[::4])

#Negative Values
print(s[-17:-1])
print(s[:-6])
print(s[-5:])
print(s[-15:-2:2])
print(s[::-1])









































